					<p>Sphere Travelmedia &amp; Exhibitions Pvt. Ltd., India’s premier and leading travel Media Company, is a pioneer in the business of branding and media services for Hospitality, Leisure and the Travel industry. Set up in 1998, we are proven forerunners in organizing product launches, brand building exercises, handling media and Public Relations services for our clients.</p>
 
					<p>‘India International Travel Mart’, India’s (IITM) – Is the flagship brand of Sphere Travelmedia. This event is a leading global event for the travel industry and is held once a year in each of the Indian cities of Bangalore, Chennai, Cochin, Delhi, Gurgaon, Hyderabad, Kolkata, Mumbai and Pune.</p>

					<p>These exhibitions bring professionals from the tourism industry together with people interested in travel. IITM is a vibrant, must attend business-to-business event presenting a diverse range of destinations and industry sectors to India and International travel professionals. It is a unique opportunity for the travel trade to meet, network, negotiate and conduct business.</p>

					<p>In the past few years IITM has seen a progressive growth of participation in the number of suppliers exhibiting, with over 200 participants both from India and overseas at many of the shows. IITM provides the right impetus to reach the vast Indian audience.</p>

					<p>The importance and success of IITM trade exhibitions can be measured by the sheer number of visitors both trade and public that they have attracted. The year 2014 surveys showed an attendance of approx 1,20,000 people to the shows collectively.</p>
